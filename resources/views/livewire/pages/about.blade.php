        <h1 class="text-4xl md:text-5xl font-bold text-center text-accent mb-10">
            Geometria Anima – Геометрия на душата
        </h1>

        <p>Това име не е просто метафора, а едно алхимично пътуване — не по картата на света, а по картата навътре, през
            пластовете на забрава, към ядрото на съзнанието. Това е пътят на душата, която не просто иска да узнае, а да
            се събуди.</p>

        <p>Идеята се роди от нуждата да се изкаже онова, което не се побира в думи — един вътрешен зов за съгласуване
            между видимото и невидимото, между материята и духа, между мисълта и формата.</p>

        <p>В своята същност това е алхимичният път на съзнанието, което се стреми не просто да знае, а да си спомни.
            Процесът, в който знанието преминава през опитност и се ражда като мъдрост. Геометрията на душата — като
            карта към Източника.</p>

        <p>В основата на проекта стои едно дълбоко разбиране: че Вселената е ментална по природа. Че всяка форма, всяко
            движение, всяко преживяване е проявление на Първичната мисъл. Че реалността не просто се случва — тя се
            създава, отвътре навън.</p>

        <p>Това пространство е създадено, за да събере символите, числата, формите и историите на всички времена в едно
            живо и практично познание. Не само с думи и философии, а с реални факти, научни методи и формули – както и
            примери от нашето ежедневие, историята и природата.</p>

        <p>За да разберем не просто как работи реалността, а как ние я сътворяваме.</p>

        <p>Защото истината не е нещо, което откриваш — тя е нещо, което си спомняш.</p>

        <p>Вселената не е безреден хаос. Тя е жив организъм, съставен от модели, повторения и принципи. И зад всяко
            движение, зад всяка промяна, стои закономерност. Скрит ред.</p>

        <p>Веднъж щом се научим да четем този език — на форма, число и пропорция — ние започваме да разбираме и себе си.
            Ние не просто съществуваме във Вселената — ние я отразяваме.</p>

        <p>Geometria Anima не е просто проект. Това е зов. Призив за завръщане към същността. Не за да се изгубим в
            звездите, а за да ги открием в себе си.</p>

        <p>И когато започнем да живеем според тези принципи — когато мисълта, словото и делата се подредят в единен
            ритъм — тогава започва истинската трансформация.</p>

        <p class="pt-4 text-center text-accent text-xl italic">Ако искаш да разбереш малко повече за човека зад този
            проект, прочети по-долу.</p>

