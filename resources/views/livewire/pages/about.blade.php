                <h2 class="text-3xl font-bold text-accent text-center mb-10">За мен</h2>

                <p>Аз съм Мартин и съм роден в далечната 1998 година в Йоханесбург, Южна Африка. Не по друга причина, а
                    защото родителите ми работеха там с цел събиране на средства. Пристигам в България изпратен със
                    самолет, поради стечения на обстоятелствата — без мама и тати — придружаван от момче на 22-23
                    години, което има задачата да се грижи за мен. Реващ и с наакани гащи кацаме на грешното летище.
                    Бабите и дядо ме чакат в София, самолетът обаче каца в Пловдив, и късметлията, който трябва да ме
                    наглежда след 12-часов полет, получава бонус — още два часа с мен, докато настойниците ми се появят.
                </p>

                <p>В едни от най-ранните ми спомени се сещам за първия път, когато видях пирамидите в Египет. Мисля, че
                    беше във филма "Мумията". В мига, в който ги зърнах, нещо дълбоко в мен се събуди. Сякаш имах
                    спомени от място, на което никога не съм бил. Не мога да опиша как се почувствах — бях дете — но не
                    бях изненадан, а впечатлен и доволен, че съм си "припомнил", въпреки че не знаех какво точно си
                    припомних. Тогава за първи път започнах тайно да мечтая, че искам да се преродя в древен Египет, във
                    фараонско семейство — не за друго, а заради знанието и технологията, които вярвах, че ще ми бъдат
                    предадени.</p>

                <p>Малко по-късно изживях първия си осъзнат сън — на 4-годишна възраст. В него бях с братовчедка ми Мая
                    на висока скала. Под нас — пропаст, изпепелени от огън цветя, земята — черна и на места
                    кървавочервена. Но горе — слънчево, зелена трева, аромат на пролет. Погледнах Мая в очите, и сякаш
                    цялата реалност се избистри. Осъзнах, че сънувам. Развълнуван, ѝ казах, че това не е реалността, че
                    сънуваме. Хванах я за ръка и скочихме. Приземихме се долу, но всичко беше зелено — изгорената поляна
                    бе станала райска. Разбрах, че имам пълен контрол и мога да творя без граници и лимити. Събудих се
                    щастлив. Но когато ѝ разказах — тя не помнеше нищо.</p>

                <p>След този сън започнах редовно да се "събуждам" насред сънищата си. Имах богато въображение,
                    прекарвах часове в размисъл и фантазии, но не можех да споделя с никого. Навън — играех роля. В
                    училище бях "тартора", шегаджията, който тормози децата и учителите. Зад това поведение се криеше
                    усещането, че никога няма да намеря човек, който да ме разбере.</p>

                <p>С времето осъзнавах, че хората около мен са по-близо до овце, отколкото до разумни същества. Не
                    разбирах как могат да живеят без да се замислят за големите въпроси — откъде идваме, кои сме, защо
                    сме тук. Как работи вселената, как всичко е започнало и изобщо защо... какъв е смисълът. Животът им
                    ми изглеждаше като смъртна присъда: раждане, училище, работа, семейство, ковчег. Хората не виждаха
                    как ги контролират и разделят. Аз реагирах на това с бунт и саморазрушение.</p>

                <p>В училище вече мислех за най-дълбоките въпроси на Вселената. Материалът, който учехме, ми беше
                    скучен. Задавах въпроси, които смущаваха учителите. Те не бяха подготвени да обсъждат философия,
                    особено когато въпросите ми засягаха истината в тях самите. Реших да вляза в ролята на "черната
                    овца" и да играя този, за когото ме смятаха. Но истината я знаех само аз.</p>

                <p>Приятелите ми не можеха да ме разберат. Техните теми — футбол, кеч, забавления — не ме вълнуваха. В
                    търсене на признание се гмурках в още по-глупави и опасни постъпки. Станах известен в квартала.
                    Учителите искаха да ме изгонят, но въпреки че не внимавах, винаги взимах изпитите.</p>

                <p>Животът ми беше водовъртеж, но вътрешният ми глас не млъкваше. Искаше истина, промяна, посока.
                    Въпреки всичко намирах време за четене. Гледах научни предавания по физика, астрофизика, биология.
                    Любопитството ми беше по-силно от болката.</p>

                <p>Минах през много версии на себе си:</p>
                <ul class="list-disc list-inside space-y-1 pl-4">
                    <li>Спортист (тенис, айкидо, кикбокс, муай тай)</li>
                    <li>Геймър и пристрастен към интернет</li>
                    <li>Пристрастен към трева, цигари, хазарт</li>
                    <li>„Лошо момче“, в очите на закона — престъпник</li>
                    <li>Вкъщи — почти невидим, исках да прекарвам възможно най-малко време там.</li>
                </ul>

                <p>Но никоя от тези версии не бях истинският аз. Него — нямах с кого да го споделя. Или поне така
                    мислех.</p>

                <p>След години на самодеструктивност и обвинения, че не живея истински към себе си, че си губя
                    потенциала, и че не правя това, което знам, че трябва — един ден нещо се промени. Пъзелът започна да
                    се нарежда. Науката – с нейните пропуски. Духовността – с нейните неясности. Изведнъж всичко се
                    свърза чрез една книга — „Изумрудените скрижали на Тот“.</p>

                <p>Тя обедини преживяванията ми, знанията ми, науката и метафизиката — без противоречия. Всичко се събра
                    в едно.</p>

                <p>Структурите и моделите в природата, физиката, астрономията, математиката — винаги ме вълнуваха, но не
                    виждах общата нишка, която ги свързваше и обясняваше всичко. Сега видях, че те говорят един и същи
                    език. Езикът на Сътворението. Езикът на Геометрията на душата. Истината винаги е била пред нас —
                    предавана от поколение на поколение.</p>

                <p>Малко по малко започнах да научавам за дълбоките процеси и закони, по които реалността твори и
                    създава всичко, което съществува. Не просто чрез духовни учения или философски размисли, а чрез
                    факти и методи на науката. Видях как физиката и квантовата механика вече са обединени — и тази
                    „теория на всичко“, която учените търсят и до днес, всъщност вече съществува.</p>

                <p>И така — от 20-годишна възраст до сега — последните седем години бяха най-дългите и с най-много
                    преживявания. Тук ще ми е трудно да обясня точно през какво преминах, затова ще го кажа наистина
                    много грубо, а по-нататък във времето ще споделям малко по малко от моите преживявания през този
                    период.</p>

                <p>Започнах да се опитвам да живея в Истината. Проведох истински разговор с родителите ми — поотделно.
                    Казах им своята истина и своята перспектива. Промених средата си, навиците си и почти всички
                    деструктивни поведения. Спрях цигарите, алкохола, наркотиците, хазарта и други.</p>

                <p>Обещах си, че повече няма да се свалям на нивото на хората около мен, нито да се насилвам да
                    прекарвам време с тях просто за да мине денят.</p>

                <p>Грабнах си една раница с дрехи и книги и отидох на гарата. Първият влак, който видях извън страната,
                    беше за Букурещ — и го хванах.</p>

                <p>Оттогава, в продължение на години, пътувах из Европа и България. Запознах се със стотици хора, които
                    не съм предполагал, че могат да съществуват. Всички — с истории, подобни на моята. Хора, преминали
                    през мрака, за да стигнат светлината. Но за мое очудване — повечето от тях бяха стигнали до сходни
                    заключения.</p>

                <p>Може би чрез различни пътища, но финалният резултат беше почти идентичен.</p>

                <p>Научих много уроци — от тях и от живота. Всяко място, на което бях доброволец, ми показваше
                    правилните хора и правилните уроци, от които имах нужда. Всяка книга, която прочетох, ме пращаше към
                    следващата — точно тази, която ми беше необходима.</p>

                <p>Пласт по пласт, започнах да разгръщам реалността — и истината зад нея. За нашите съзнания. За душите.
                    За геометрията на Вселената и за езика, чрез който тя твори.</p>

                <p>Но за да не звуча арогантно тук, където няма как да обясня в пълнота какво точно имам предвид — ще
                    спрем дотук.</p>


                <p>Този проект е не просто идея — той е плод на личен път, на вътрешни търсения, съмнения, прозрения и
                    трансформации. Той е резултат от години изследвания, преживявания и дълбоко наблюдение върху
                    реалността.</p>

                <p><strong>Geometria Anima – Геометрия на душата</strong> има мисия:</p>

                <ul class="list-disc list-inside space-y-1 pl-4">
                    <li>Да разруши границата между наука и духовност.</li>
                    <li>Да съедини митологията с историята.</li>
                    <li>Да свърже разума със сърцето.</li>
                </ul>

                <p>Всичко това – с една цел:
                    да бъде подадена ръка на онези, които преминават през същите вътрешни етапи,
                    както аз и много други преди мен… и които са вярвали, че са сами.</p>

                <p>Ако си един от тях — знай, че не си сам.
                    Отговорите, които търсиш, не се крият далече —
                    те винаги са били в теб.</p>

                <p class="font-semibold text-accent mt-6">Оттук започва всичко.</p>

                <p><strong>Нашата отправна точка:</strong></p>

                <ul class="list-disc list-inside space-y-1 pl-4">
                    <li>Всички сме Едно.</li>
                    <li>Вселената е цялостен жив организъм.</li>
                    <li>Съзнанието не просто възприема реалността — то я създава.</li>
                </ul>

                <p class="text-xl text-accent/90 mt-6 italic">А когато си спомним кои сме — и откъде идваме — всичко
                    става възможно.</p>

